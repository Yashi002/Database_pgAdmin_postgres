	furniture_id INTEGER ,
	room_id INTEGER,
	PRIMARY KEY (furniture_id,room_id),
	FOREIGN KEY(furniture_id)
	REFERENCES furniture(furniture_id)
	ON DELETE CASCADE,
	FOREIGN KEY(room_id)
	REFERENCES room(room_id)
	ON DELETE CASCADE
	
